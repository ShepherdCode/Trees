<h1>Trees</h1>
The Shepherd Trees project provides information about
every tree on the campus of Shepherd University.
The information gathered by 
Shepherd students and faculty
includes biological, physical, and historical details.
Beginning in 2019, the information will be accessible via the web and via a mobile app.
Shepherd Trees is a project of the 
College of Natural Sciences & Mathematics 
(<a href="http://www.shepherd.edu/snsm">CNSM</a>)
at <a href="http://www.shepherd.edu/">Shepherd University</a>.
